package Demo;

public class AddSubMulDivMod {
	static void add()
	{
		int a=12;
		int b=10;
		System.out.println(a+b);		
	}
	static void sub() 
	{
		int c=20;
		int d=25;
		System.out.println(c-d);				
	}
	static void mul()
	{
		int e=10;
		int f=20;
		System.out.println(e*f);
	}
	static void div()
	{
		int g=100;
		int h=12;
		System.out.println(g/h);	
	}
	static void mod()
	{
		int a1=11;
		int a2=2;
		System.out.println(a1%a2);
	}
	public static void main(String[] args) {
	     add();
	     sub();
	     mul();
	     div();
	     mod();

	}

}
